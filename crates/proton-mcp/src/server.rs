#[cfg(test)]
mod tests {
    use super::*;

    use rmcp::handler::server::wrapper::Parameters;
    use serde_json::json;

    use proton_core::Verdict;

    use crate::common::{excerpt, should_perform, signature_warning};
    use crate::tools::delete_messages::DeleteMessagesParams;
    use crate::tools::empty_folder::EmptyFolderParams;
    use crate::tools::move_conversation::MoveConversationParams;
    use crate::tools::move_message::MoveMessageParams;
    use crate::tools::send_message::SendMessageParams;

    const NO_SESSION_PROFILE: &str = "proton-mcp-test-nonexistent-profile-zz9q";

    #[test]
    fn gating_dry_run_when_not_allowed_and_unconfirmed() {
        // Default posture: no --allow-writes, no confirm => preview only.
        assert!(!should_perform(false, None));
        assert!(!should_perform(false, Some(false)));
        // Explicit confirm performs the action.
        assert!(should_perform(false, Some(true)));
        // --allow-writes performs regardless of confirm.
        assert!(should_perform(true, None));
        assert!(should_perform(true, Some(false)));
    }

    #[test]
    fn excerpt_truncates_with_ellipsis() {
        assert_eq!(excerpt("hello", 10), "hello");
        let e = excerpt("hello world", 5);
        assert!(e.starts_with("hello"));
        assert!(e.ends_with('…'));
    }

    #[test]
    fn signature_warning_only_for_unverified() {
        assert!(signature_warning(Verdict::Verified).is_none());
        assert!(signature_warning(Verdict::Unsigned).is_some());
        assert!(signature_warning(Verdict::Unverified).is_some());
        assert!(signature_warning(Verdict::Invalid).is_some());
    }

    #[tokio::test]
    async fn tool_without_session_returns_not_authenticated() {
        let server = ProtonMail::new(NO_SESSION_PROFILE.to_string(), false);
        // A read tool with no stored session must surface the actionable error,
        // not panic.
        let err = match server.list_labels().await {
            Ok(_) => panic!("expected a not-authenticated error, got Ok"),
            Err(e) => e,
        };
        assert!(
            err.message.contains("Not authenticated"),
            "unexpected error message: {}",
            err.message
        );
        assert!(err.message.contains(NO_SESSION_PROFILE));
    }

    #[tokio::test]
    async fn send_returns_dry_run_preview_when_unconfirmed() {
        let server = ProtonMail::new(NO_SESSION_PROFILE.to_string(), false);
        let params = SendMessageParams {
            to: vec!["alice@example.com".to_string()],
            cc: Vec::new(),
            bcc: Vec::new(),
            from: None,
            subject: "Greetings".to_string(),
            body: "Hello there, this is the body.".to_string(),
            html: None,
            send_at: None,
            expires: None,
            confirm: None,
        };
        // No session is configured, but the dry-run path must short-circuit
        // before touching the client, so this succeeds without auth.
        let out = server
            .send_message(Parameters(params))
            .await
            .expect("dry-run preview should be Ok");
        let v = out.0;
        assert_eq!(v["dry_run"], json!(true));
        assert_eq!(v["action"], json!("send_message"));
        assert_eq!(v["preview"]["subject"], json!("Greetings"));
        assert_eq!(v["preview"]["to"][0], json!("alice@example.com"));
    }

    #[tokio::test]
    async fn organize_dry_run_preview_lists_ids_and_target() {
        let server = ProtonMail::new(NO_SESSION_PROFILE.to_string(), false);
        let params = MoveMessageParams {
            references: vec!["id-one".to_string(), "id-two".to_string()],
            folder: "archive".to_string(),
            confirm: None,
        };
        let out = server
            .move_message(Parameters(params))
            .await
            .expect("dry-run preview should be Ok");
        let v = out.0;
        assert_eq!(v["dry_run"], json!(true));
        assert_eq!(v["preview"]["folder"], json!("archive"));
        assert_eq!(v["preview"]["references"][1], json!("id-two"));
    }

    #[tokio::test]
    async fn empty_folder_dry_run_warns_and_does_not_perform() {
        // A permanent, irreversible action must short-circuit to a preview
        // (with a warning) when unconfirmed — never touching the client.
        let server = ProtonMail::new(NO_SESSION_PROFILE.to_string(), false);
        let out = server
            .empty_folder(Parameters(EmptyFolderParams {
                folder: "trash".to_string(),
                confirm: None,
            }))
            .await
            .expect("dry-run preview should be Ok");
        let v = out.0;
        assert_eq!(v["dry_run"], json!(true));
        assert_eq!(v["action"], json!("empty_folder"));
        assert_eq!(v["preview"]["folder"], json!("trash"));
        assert!(v["preview"]["warning"].is_string());
    }

    #[tokio::test]
    async fn delete_messages_dry_run_when_unconfirmed() {
        let server = ProtonMail::new(NO_SESSION_PROFILE.to_string(), false);
        let out = server
            .delete_messages(Parameters(DeleteMessagesParams {
                references: vec!["m1".to_string()],
                confirm: None,
            }))
            .await
            .expect("dry-run preview should be Ok");
        let v = out.0;
        assert_eq!(v["dry_run"], json!(true));
        assert_eq!(v["action"], json!("delete_messages"));
        assert_eq!(v["preview"]["references"][0], json!("m1"));
    }

    #[tokio::test]
    async fn move_conversation_dry_run_lists_ids_and_target() {
        let server = ProtonMail::new(NO_SESSION_PROFILE.to_string(), false);
        let out = server
            .move_conversation(Parameters(MoveConversationParams {
                ids: vec!["c1".to_string(), "c2".to_string()],
                folder: "archive".to_string(),
                confirm: None,
            }))
            .await
            .expect("dry-run preview should be Ok");
        let v = out.0;
        assert_eq!(v["dry_run"], json!(true));
        assert_eq!(v["action"], json!("move_conversation"));
        assert_eq!(v["preview"]["folder"], json!("archive"));
        assert_eq!(v["preview"]["ids"][1], json!("c2"));
    }
}
