//! The `ProtonMail` MCP server handler: shared state, tool router, and the
//! `ServerHandler` implementation.
//!
//! The Proton [`Client`] is held lazily behind an async mutex and resumed on
//! first use. Destructive write tools are confirm-gated: unless the server was
//! started with `--allow-writes` or the call passes `confirm: true`, they
//! return a dry-run preview instead of performing the action.
//!
//! Each tool lives in its own module under [`crate::tools`]; this file just
//! sums their per-tool routers together and wires up the shared state and
//! transport.

use std::sync::Arc;

use rmcp::handler::server::router::tool::ToolRouter;
use rmcp::{tool_handler, ErrorData, ServerHandler};
use tokio::sync::Mutex;

use proton_core::Client;

use crate::common::not_authenticated;

/// Shared, cheaply-clonable server state.
#[derive(Clone)]
pub struct ProtonMail {
    pub(crate) state: Arc<AppState>,
    tool_router: ToolRouter<ProtonMail>,
}

pub(crate) struct AppState {
    /// The resumed Proton client (lazily initialised on first tool use).
    pub(crate) client: Mutex<Option<Client>>,
    /// Session profile to resume (created via `protonmail-cli login`).
    profile: String,
    /// When true, destructive tools execute without requiring `confirm: true`.
    pub(crate) allow_writes: bool,
}

// --------------------------------------------------------------------------
// Construction & internal helpers
// --------------------------------------------------------------------------

impl ProtonMail {
    pub fn new(profile: String, allow_writes: bool) -> Self {
        // Combine the per-tool routers (each generated by a `#[tool_router]`
        // impl block in its own module) into the single dispatch router.
        let tool_router = Self::list_messages_router()
            + Self::search_messages_router()
            + Self::read_message_router()
            + Self::list_conversations_router()
            + Self::read_conversation_router()
            + Self::list_attachments_router()
            + Self::list_labels_router()
            + Self::send_message_router()
            + Self::reply_message_router()
            + Self::forward_message_router()
            + Self::cancel_scheduled_send_router()
            + Self::move_message_router()
            + Self::trash_message_router()
            + Self::mark_read_router()
            + Self::star_message_router()
            + Self::apply_label_router()
            + Self::remove_label_router()
            + Self::download_attachment_router()
            + Self::list_drafts_router()
            + Self::list_filters_router()
            + Self::list_contacts_router()
            + Self::list_addresses_router()
            + Self::search_local_router()
            + Self::save_draft_router()
            + Self::sync_cache_router()
            // message writes
            + Self::report_spam_router()
            + Self::report_ham_router()
            + Self::unsubscribe_router()
            + Self::empty_folder_router()
            + Self::delete_messages_router()
            + Self::undelete_messages_router()
            + Self::send_read_receipt_router()
            // drafts
            + Self::update_draft_router()
            + Self::delete_draft_router()
            // conversations
            + Self::search_conversations_router()
            + Self::move_conversation_router()
            + Self::trash_conversation_router()
            + Self::mark_conversation_router()
            + Self::star_conversation_router()
            + Self::snooze_conversation_router()
            + Self::unsnooze_conversation_router()
            // filters
            + Self::create_filter_router()
            + Self::check_filter_router()
            + Self::delete_filter_router()
            + Self::enable_filter_router()
            + Self::disable_filter_router()
            // labels / folders
            + Self::create_label_router()
            + Self::delete_label_router()
            + Self::update_label_router()
            // settings / addresses / contacts / counts / index
            + Self::get_settings_router()
            + Self::set_sign_router()
            + Self::set_attach_public_key_router()
            + Self::update_address_router()
            + Self::list_contact_emails_router()
            + Self::counts_router()
            + Self::index_cache_router();

        Self {
            state: Arc::new(AppState {
                client: Mutex::new(None),
                profile,
                allow_writes,
            }),
            tool_router,
        }
    }

    /// Map a `proton-core` error into an MCP error, surfacing auth failures
    /// (no session / expired) as the actionable "not authenticated" message.
    pub(crate) fn map_err(&self, e: proton_core::Error) -> ErrorData {
        use proton_core::Error as E;
        match &e {
            E::Unauthorized => not_authenticated(&self.state.profile),
            E::Api(api) if matches!(api.http_status, 401 | 403) => {
                not_authenticated(&self.state.profile)
            }
            _ => ErrorData::internal_error(e.to_string(), None),
        }
    }

    /// Ensure the lazily-resumed client is present in `slot`.
    pub(crate) async fn ensure(&self, slot: &mut Option<Client>) -> Result<(), ErrorData> {
        if slot.is_none() {
            let client = Client::resume(&self.state.profile)
                .await
                .map_err(|e| self.map_err(e))?;
            *slot = Some(client);
        }
        Ok(())
    }

    /// Resolve a free-text reference (or exact id) to a message id.
    pub(crate) async fn resolve(
        &self,
        client: &Client,
        reference: &str,
    ) -> Result<String, ErrorData> {
        client
            .resolve_ref(reference)
            .await
            .map_err(|e| self.map_err(e))
    }

    /// Resolve a batch of references to ids.
    pub(crate) async fn resolve_all(
        &self,
        client: &Client,
        refs: &[String],
    ) -> Result<Vec<String>, ErrorData> {
        let mut ids = Vec::with_capacity(refs.len());
        for r in refs {
            ids.push(self.resolve(client, r).await?);
        }
        Ok(ids)
    }
}

// --------------------------------------------------------------------------
// ServerHandler (call_tool / list_tools / get_info generated by the macro)
// --------------------------------------------------------------------------

#[tool_handler(
    router = self.tool_router,
    name = "proton-mcp",
    instructions = "Proton Mail tools backed by a stored protonmail-cli session. Authenticate by running `protonmail-cli login` first; this server resumes that session. Read tools are always available. Destructive/structural write tools (send, reply, forward, cancel, move, trash, delete, empty-folder, undelete, spam/ham, unsubscribe, read-receipt, draft edit/delete, conversation move/trash, label apply/remove/create/delete/update, filter create/delete/enable/disable, settings, address update) return a dry-run preview unless called with confirm=true or the server was started with --allow-writes. Benign toggles (mark read/unread, star, snooze) and reads execute directly. Message tools accept free-text references resolved to ids; conversation/filter/label/draft tools take exact ids. Sending defaults to your primary address (first alias by Order) — pass `from` to use a different alias."
)]
impl ServerHandler for ProtonMail {}

// --------------------------------------------------------------------------
// HTTP transport
// --------------------------------------------------------------------------

/// Serve the MCP server over Streamable HTTP, mounted at `/mcp`.
pub async fn serve_http(server: ProtonMail, addr: &str) -> anyhow::Result<()> {
    use rmcp::transport::streamable_http_server::{
        session::local::LocalSessionManager, StreamableHttpService,
    };

    let service = StreamableHttpService::new(
        move || Ok(server.clone()),
        Arc::new(LocalSessionManager::default()),
        Default::default(),
    );
    let app = axum::Router::new().nest_service("/mcp", service);
    let listener = tokio::net::TcpListener::bind(addr).await?;
    axum::serve(listener, app).await?;
    Ok(())
}

